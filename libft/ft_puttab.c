#include "libft.h"

void	ft_puttab(char **tab)
{
	int	i;
	int j;

	i = 0;
	j = 0;
	while (tab[i] != '\0')
	{
		while (tab[i][j] != '\0')
		{
			ft_putchar(tab[i][j]);
			j++;
		}
		ft_putchar('\n');
		j = 0;
		i++;
	}
}
